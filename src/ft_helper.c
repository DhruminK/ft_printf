#include "../includes/ft_printf.h"

void	ft_tolowercase(char *str)
{
	if (!str)
		return ;
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str - 'A' + 'a';
		str = str + 1;
	}
}
